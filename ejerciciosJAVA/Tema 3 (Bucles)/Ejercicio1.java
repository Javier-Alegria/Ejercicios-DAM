package Ejercicios4Bucles;
/*1. Escribe un programa que devuelva por pantalla la palabra "hola" 10 veces. El
programa tiene que tener menos de 10 instrucciones
 */
public class Ejercicio1 {
    public static void main(String[] args) {
        for (int i=0; i <= 9; i++) {
            System.out.println((i+1)+": Hola");
        }
    }
}
